public class Montaria {
    private String nome;
    private  int level;
    private  String type;

    private int Personagem_id;

    public Montaria(String nome, int level, String type, int Personagem_id) {
        this.nome = nome;
        this.level = level;
        this.type = type;
        this.Personagem_id = Personagem_id;
    }
}
